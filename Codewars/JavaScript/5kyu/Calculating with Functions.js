function zero(b) {
  if (b==undefined){
    return 0
  }
  else{
    let b1 = b[0]
    let b2 = parseInt(b[1])
    switch(b1){
      case '+':
        return 0 + b2
        break
      case '-':
        return 0 - b2
        break
      case '*':
        return 0 * b2
        break
      case '/':
        return Math.floor(0 / b2)
    }
  }
}
